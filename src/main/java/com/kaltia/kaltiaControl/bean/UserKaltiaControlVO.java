package com.kaltia.kaltiaControl.bean;

public class UserKaltiaControlVO {
	
	private String idUser;
	private String userUser;
	private String passUser;
	private String nombreUser;
	private String empresaUser;
	private String perfilUser;
	
	
	public String getIdUser() {
		return idUser;
	}
	public void setIdUser(String idUser) {
		this.idUser = idUser;
	}
	public String getUserUser() {
		return userUser;
	}
	public void setUserUser(String userUser) {
		this.userUser = userUser;
	}
	public String getPassUser() {
		return passUser;
	}
	public void setPassUser(String passUser) {
		this.passUser = passUser;
	}
	public String getNombreUser() {
		return nombreUser;
	}
	public void setNombreUser(String nombreUser) {
		this.nombreUser = nombreUser;
	}
	public String getEmpresaUser() {
		return empresaUser;
	}
	public void setEmpresaUser(String empresaUser) {
		this.empresaUser = empresaUser;
	}
	public String getPerfilUser() {
		return perfilUser;
	}
	public void setPerfilUser(String perfilUser) {
		this.perfilUser = perfilUser;
	}
	
	

}
